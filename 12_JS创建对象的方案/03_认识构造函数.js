// 构造器   constructor   是我们创建对象时候调用的函数
// 构造函数也是一个普通的函数,和别的函数没有区别
function foo() {
  console.log('wh')
}

foo()
// 使用 new 来调用
// 如果一个普通函数被 new 操作符来调用了,那么就是一个构造函数

// 区别：
// 使用new 来调用函数时，可以不使用（），但由于我们通常要传参数，所以一般都使用（）

// 使用new 的过程
// 1.在内存中创建一个新对象
// 2.原型赋值  新对象内部的 [[prototype]] __proto__  会指向 该构造函数的prototype属性
// 3.this绑定为创建的新对象
// 4.执行函数体内部的代码
// 5.自动返回刚刚创建的对象，所以不需要返回值

var f = new foo()
console.log(f) 